#r_datatype
#logical data type
v<-TRUE
print(class(v))
print(v)

#Numeric data type
v<-23.5
print(class(v))
print(v)

#Integer datatype
v<-2
print(class(v))
print(v)

#complex data type
v<-2+5i
print(class(v))
print(v)

#char data type
c<-"TRUE"
print(class(v))
print(v)

#Raw data type
v<-charToRaw("A")
print(class(v))
print(v)

