#Create a sequence of numbers from 32 to 44
print(seq(32,44))

#Find mean of numbers form 25 to 82
print(mean(25:82))

#Find sum of numbers from 41 to 68
print(sum(41:68))

#Find absolute value of a number
#	abs(x): It returns the absolute value of input x.
print(abs(-4))

#Find square root of a number
#sqrt(x)
print(sqrt(25))

#Find round value of a number
#round(x, digits=n)
print(round(4.465767,digits=3))

#Find exponent of a number
#exp()
print(exp(4))

#substr(x, start=n1,stop=n2)
#It is used to extract substrings in a character vector.
a="Payal Makwana"
print(substr(a, start=0,stop=5))

#sub(pattern replacement, x, ignore.case =FALSE, fixed=FALSE)
#It finds pattern in x and replaces it with replacement (new) text.
str1 = "Programming needs no practice"
print(sub("no","daily",str1))

#strsplit(x, split)
#It splits the elements of character vector x at split point.It splits the elements of character vector x at split point.
str2 = "Payal Makwana"
print(strsplit(str2, " "))


